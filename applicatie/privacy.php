        <h1>Privacyverklaring</h1>
        <h2>Overzicht van de manier waarop we uw gegevens gebruiken</h2>
        <p>Deze Privacyverklaring beschrijft hoe wij, pizza sola machina B.V., (ook aangeduid als "Sola Machina " of "ons") persoonsgegevens van de klanten verzamelen, gebruiken, beschermen en delen.</p>
        <p>Sola Machina, die de websites, mobiele applicaties en activiteiten op de sociale media (“online diensten”) beheert, is de verwerkingsverantwoordelijke voor uw informatie.</p>
        <p>Sola Machina verzamelt en verwerkt uw persoonsgegevens wanneer u onze online diensten gebruikt, als u per telefoon of in een winkel van Sola Machina een bestelling plaatst, of als u op een andere manier met ons communiceert.</p>
        <p>Sola Machina gebruikt uw persoonsgegevens voornamelijk:</p>
        <ul>
            <li>om uw bestellingen te verwerken;</li>
            <li>voor het verstrekken van online diensten waaronder klantaccounts;</li>
            <li>voor het beheer van klantenrelaties: vragen beantwoorden, vorderingen verwerken, feedback verzamelen, informatie verzamelen over nieuwe producten, aanbiedingen en diensten en het communiceren van aanbiedingen en acties;</li>
            <li>om gepersonaliseerde advertenties aan te reiken; en</li>
            <li>om analyses uit te voeren.</li>
        </ul>
        <p>Persoonsgegevens worden gedeeld met (i) ons moederbedrijf en de bedrijvengroep van Sola Machina, (ii) onze zelfstandige franchisenemers en onze eigen winkels die betrokken zijn bij de verwerking van uw bestellingen en (iii) onze dienstverleners.</p>
        <p>Indien wij uw toestemming nodig hebben, bijvoorbeeld bij het plaatsen van cookies of vergelijkbare technologieën, kunt u uw toestemming op elk moment intrekken.</p>
        <p>In deze Privacyverklaring wordt meer informatie gegeven over de verwerking van uw persoonsgegevens en uw rechten in dit verband, met inbegrip van het recht om bezwaar te maken tegen bepaalde onderdelen van de door Sola Machina uitgevoerde verwerking. U vindt meer informatie over uw rechten en hoe u deze kunt uitoefenen in "Welke rechten heb ik?".</p>
        
        <h2>Wat behandelt deze verklaring niet?</h2>
        <p>Veel van onze winkels worden door franchisenemers geëxploiteerd. Deze franchisenemers zijn zelfstandige ondernemers en daarom ook zelfstandig verwerkingsverantwoordelijke voor de verwerking van uw persoonsgegevens. Deze Privacyverklaring geldt niet voor onze franchisenemers aangezien elke franchisenemer zelf verantwoordelijk is voor de naleving van privacywetgeving. Voor meer informatie over hun privacy- en gegevensbeschermingspraktijken, waaronder ten aanzien van persoonsgegevens die per telefoon en/of in de winkel zijn verstrekt, verwijzen wij u naar de privacyverklaring van de betreffende franchisenemer, welke wordt getoond in de winkel.</p>
        
        <h2>Hoe verzamelt Pizzaria sola Machina uw gegevens?</h2>
        <p>Deze Privacyverklaring is van toepassing op de persoonsgegevens die we van of over u verzamelen via de volgende methoden:</p>
        <ul>
            <li>Online omgeving. Op de consument gerichte websites en applicaties van Sola Machina, waaronder mini-websites die we beheren op sociale netwerken van derden zoals Facebook.</li>
            <li>E-mail, sms-berichten en andere elektronische communicatie.</li>
            <li>Social media activiteiten.</li>
            <li>Bezorgdiensten. We ontvangen de gegevens over uw bestelling via bezorgdiensten en platformen zoals Thuisbezorgd.</li>
            <li>Spraakdiensten. We ontvangen de gegevens over uw bestelling via spraakdiensten zoals Google Home.</li>
        </ul>
        <p>Opgelet: persoonsgegevens, die we via één methode verzamelen (bijv. via een website van Sola Machina), combineren we met persoonsgegevens die we via een andere methode verzamelen (bijv. communicatie met Sola Machina).</p>
        
        <h2>Welke informatie verzamelen wij?</h2>
        <p>Persoonsgegevens:</p>
        <ul>
            <li>uw voor- en achternaam;</li>
            <li>bijzonderheden over uw bestelling, waaronder facturatie- en besteladressen;</li>
            <li>uw thuisadres, e-mailadres en telefoonnummer;</li>
            <li>gekochte producten en diensten, prijzen;</li>
            <li>informatie over de rekening die u gebruikt voor toegang tot onze online diensten, het kopen of gebruiken van onze producten en diensten (gebruikersnaam, wachtwoord en soortgelijke gegevens);</li>
            <li>uw marketingvoorkeuren, waaronder alle toestemming die u ons hebt gegeven (indien gevraagd);</li>
            <li>informatie over uw profiel (populaire producten en diensten, tijdstippen waarop u ons bezoekt enz.);</li>
            <li>informatie betreffende de browser of het apparaat dat u gebruikt voor toegang tot onze website/applicaties, waaronder uw IP-adres.</li>
            <li>andere persoonsgegevens die u met uw instemming aan ons doorgeeft wanneer u met ons communiceert.</li>
        </ul>

        <h2>Hoe gebruiken we deze informatie en wat is de rechtsgrondslag voor dit gebruik?</h2>
        <p>We verwerken deze persoonsgegevens voor de volgende doeleinden:</p>
        <ul>
            <li>Voor het nakomen van een contract, of om maatregelen te nemen in verband met een contract: dit is relevant als u een account aanmaakt, een bestelling plaatst of meedoet aan een door ons georganiseerde prijsvraag.</li>
            <li>Zoals vereist door Sola Machina om zaken te doen en onze gerechtvaardigde belangen na te streven.</li>
            <li>Waar u ons toestemming voor geeft.</li>
            <li>Voor toepassingen die wettelijk verplicht zijn.</li>
        </ul>

